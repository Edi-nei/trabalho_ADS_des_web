print("PARA SABER SE O NUMERO É POSSITIVO, NEGATIVO OU NULO")
n1 = float (input("Digite o numero: "))
if n1 > 0:
    print("Seu numero é positivo!")
elif n1 < 0:
    print("Seu numero é negativo!")
else: print("Seu numero é NULO")

